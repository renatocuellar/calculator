class calculatorAddition {
    addition(num1, num2){
        return num1 + num2
    }
};

class calculatorSubtraction {
    subtraction(num1, num2){
        return num1 - num2
    }
};

class calculatorMultiplication {
    multiplication(num1, num2){
        return num1 * num2
    }
};

class calculatorDivision {
    division(num1, num2){
        return num1 / num2
    }
};
